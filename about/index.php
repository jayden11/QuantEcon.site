<h1>About</h1>

<p>QuantEcon is a community based organization run by economists 
and backed by the <a href="http://www.sloan.org/">Alfred P. Sloan Foundation</a>.
Its aim is to coordinate development of high quality open source code for
quantitative economic modeling.</p>

<p>While some of the code in QuantEcon was initially written as part of the
lectures at <a href="http://quant-econ.net">quant-econ.net</a>, the library is
an entirely separate entity with a life of its own that welcomes collaboration with interested
parties.</p>


